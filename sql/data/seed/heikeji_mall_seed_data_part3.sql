-- ----------------------------
-- 社区板块表
-- ----------------------------
CREATE TABLE IF NOT EXISTS `boards` (
  `id` BIGINT NOT NULL AUTO_INCREMENT COMMENT '板块ID',
  `name` VARCHAR(50) NOT NULL COMMENT '板块名称',
  `description` VARCHAR(255) DEFAULT NULL COMMENT '板块描述',
  `icon` VARCHAR(200) DEFAULT NULL COMMENT '板块图标URL',
  `sort_order` INT DEFAULT 0 COMMENT '排序序号',
  `post_count` INT DEFAULT 0 COMMENT '帖子数量',
  `status` TINYINT(1) DEFAULT 1 COMMENT '状态:1-启用,0-禁用',
  `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `updated_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  PRIMARY KEY (`id`),
  KEY `idx_status` (`status`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='社区板块表';

-- ----------------------------
-- 社区帖子表
-- ----------------------------
CREATE TABLE IF NOT EXISTS `posts` (
  `id` BIGINT NOT NULL AUTO_INCREMENT COMMENT '帖子ID',
  `board_id` BIGINT NOT NULL COMMENT '板块ID',
  `user_id` BIGINT NOT NULL COMMENT '发帖用户ID',
  `title` VARCHAR(200) NOT NULL COMMENT '帖子标题',
  `content` TEXT NOT NULL COMMENT '帖子内容',
  `images` TEXT DEFAULT NULL COMMENT '图片列表(JSON数组)',
  `view_count` INT DEFAULT 0 COMMENT '浏览量',
  `like_count` INT DEFAULT 0 COMMENT '点赞数',
  `comment_count` INT DEFAULT 0 COMMENT '评论数',
  `is_top` TINYINT(1) DEFAULT 0 COMMENT '是否置顶:0-否,1-是',
  `is_essence` TINYINT(1) DEFAULT 0 COMMENT '是否精华:0-否,1-是',
  `status` TINYINT(1) DEFAULT 1 COMMENT '状态:1-正常,0-隐藏,2-已删除',
  `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `updated_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  PRIMARY KEY (`id`),
  KEY `idx_board` (`board_id`),
  KEY `idx_user` (`user_id`),
  KEY `idx_status` (`status`),
  KEY `idx_created_at` (`created_at`),
  CONSTRAINT `fk_post_board` FOREIGN KEY (`board_id`) REFERENCES `boards` (`id`) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='社区帖子表';

-- ----------------------------
-- 社区评论表
-- ----------------------------
CREATE TABLE IF NOT EXISTS `comments` (
  `id` BIGINT NOT NULL AUTO_INCREMENT COMMENT '评论ID',
  `post_id` BIGINT NOT NULL COMMENT '帖子ID',
  `user_id` BIGINT NOT NULL COMMENT '评论用户ID',
  `parent_id` BIGINT DEFAULT NULL COMMENT '父评论ID(回复用)',
  `content` VARCHAR(1000) NOT NULL COMMENT '评论内容',
  `like_count` INT DEFAULT 0 COMMENT '点赞数',
  `status` TINYINT(1) DEFAULT 1 COMMENT '状态:1-正常,0-隐藏',
  `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `updated_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  PRIMARY KEY (`id`),
  KEY `idx_post` (`post_id`),
  KEY `idx_user` (`user_id`),
  KEY `idx_parent` (`parent_id`),
  CONSTRAINT `fk_comment_post` FOREIGN KEY (`post_id`) REFERENCES `posts` (`id`) ON DELETE CASCADE,
  CONSTRAINT `fk_comment_parent` FOREIGN KEY (`parent_id`) REFERENCES `comments` (`id`) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='社区评论表';

-- ----------------------------
-- 校园活动表
-- ----------------------------
CREATE TABLE IF NOT EXISTS `activities` (
  `id` BIGINT NOT NULL AUTO_INCREMENT COMMENT '活动ID',
  `title` VARCHAR(200) NOT NULL COMMENT '活动标题',
  `description` TEXT NOT NULL COMMENT '活动详情描述',
  `cover_image` VARCHAR(500) DEFAULT NULL COMMENT '封面图片URL',
  `location` VARCHAR(200) DEFAULT NULL COMMENT '活动地点',
  `start_time` DATETIME NOT NULL COMMENT '开始时间',
  `end_time` DATETIME NOT NULL COMMENT '结束时间',
  `registration_start` DATETIME DEFAULT NULL COMMENT '报名开始时间',
  `registration_end` DATETIME DEFAULT NULL COMMENT '报名截止时间',
  `max_participants` INT DEFAULT NULL COMMENT '最大参与人数',
  `current_participants` INT DEFAULT 0 COMMENT '当前报名人数',
  `organizer` VARCHAR(100) DEFAULT NULL COMMENT '主办方',
  `contact_phone` VARCHAR(20) DEFAULT NULL COMMENT '联系电话',
  `status` TINYINT(1) DEFAULT 1 COMMENT '状态:0-未开始,1-报名中,2-进行中,3-已结束,4-已取消',
  `is_top` TINYINT(1) DEFAULT 0 COMMENT '是否置顶推荐',
  `created_by` BIGINT DEFAULT NULL COMMENT '创建人用户ID',
  `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `updated_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  PRIMARY KEY (`id`),
  KEY `idx_status` (`status`),
  KEY `idx_start_time` (`start_time`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='校园活动表';


